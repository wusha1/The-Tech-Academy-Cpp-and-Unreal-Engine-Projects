#include <iostream>
#include <cmath>

int main()
{
    // This program shows four common math functions available through the <cmath> library.

    std::cout << "Max of 10 and 20: " << std::max(10, 20) << "\n";
    std::cout << "Square root of 64: " << std::sqrt(64) << "\n";
    std::cout << "Rounded value of 9.6: " << std::round(9.6) << "\n";
    std::cout << "2 raised to the power of 5: " << std::pow(2, 5) << "\n";

    return 0;
}